package org.hansu;

public class CpaAndTcpa {

    private double cpa;
    private double tcpa;

    public CpaAndTcpa(double cpa, double tcpa) {
        this.cpa = cpa;
        this.tcpa = tcpa;
    }

    public double getCpa() {
        return cpa;
    }

    public double getTcpa() {
        return tcpa;
    }
}
